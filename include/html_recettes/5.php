<p> <strong>Préparation :</strong><br>
	Dans un récipient mettre les 2 oeufs battus, la crème, le sucre vanillé<br>
	Dérouler la pâte dans le moule avec le papier cuisson.<br>
Eplucher les poires , les couper en 2 ou en 4 et les disposer.<br>
