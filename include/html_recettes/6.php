<p> <strong>Préparation :</strong><br>
	Etalez la pâte à pizza sur une plaque puis recouvrez-la d'une fine couche de crème fraîche.<br>
	Disposez les tranches de saumon, de façon à ce qu'elles recouvrent toute la pâte.<br>
	Arrosez de 2 c.a.s d'huile d'olive, saupoudrez d'origan et de quelques zestes de citron.<br>
Coupez la mozzarella en tranches moyennes pour en recouvrir la pizza.<br>
Enfournez dans un four préchauffé à 200°C .
