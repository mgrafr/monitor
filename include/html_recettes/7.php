<p> <strong>Préparation :</strong><br>
	Dans une poêle, faire rissolé les diots avec la matière grasse, les pré-cuire.<br>
	Dans la même poêle , ajouter les oignons et les échalottes .<br>
	Quand les oignons et échalottes sont dorés ajouter le vin blanc et ensuite la craime fraiche..<br>
Mettre les diots dans un plat , les recouvrir de la moitié de la sauce crème-oignons-échalottes.<br>
Mettre dans un autre plat les P de T coupées en rondelles en alternant les couches avec le fromage et couvrir du restant de sauce<br>
	Mettre au four à 200° 40 Minutes	</p>