<p> <strong>Préparation :</strong><br>
	Les ingrédients doivent être à température ambiante. Mélangez le jaune d'oeuf, un peu de sel, poivre, la moutarde et le vinaigre.<br>
Fouetter en versant peu à peu l'huile, la mayonnaise doit peu à peu épaissir.<br>
On peut y ajouter des herbes ou du citron pour la parfumer.
