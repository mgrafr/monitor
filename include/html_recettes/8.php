<p> <strong>Préparation :</strong><br>
	Précuire les pommes de terre, les couper en lamelles
	Dans une poêle, faire revenir les lardons et les oignons avec la matière grasse.<br>
	Dans un plat à gratin beurré , superposer plusieurs couches de p.d.t, lardons et oignons .<br>
	Recouvrir ensuite de crême fraiche et étaler des lamelles de fromage.<br><br>
	Faire cuire au four à 210° pendant 25 minutes.... quand le fromage est gratiné!!<br>
